// Function to apply translations
// Store the original text of elements on page load
function storeOriginalText() {
    const originalTexts = {};
    document.querySelectorAll('[id$="_dynamiq"]').forEach(element => {
        originalTexts[element.id] = element.innerText;
    });
    sessionStorage.setItem('originalTexts', JSON.stringify(originalTexts));
}

async function applyTranslations(language) {
    const languageDropdown = document.getElementById('language-dropdown');
    const selectedLanguage = sessionStorage.getItem('selectedLanguage');
    if (selectedLanguage) {
        languageDropdown.value = selectedLanguage;
    }
    // Retrieve the previous language from sessionStorage
    const previousLanguage = sessionStorage.getItem('previousLanguage');
    const originalTexts = JSON.parse(sessionStorage.getItem('originalTexts') || '{}');

    // Check if the current language matches the previous language
    if (language === previousLanguage) {
        // Reset elements to their original values
        Object.keys(originalTexts).forEach(id => {
            const element = document.getElementById(id);
            if (element) {
                element.innerText = originalTexts[id];
            }
        });
        return; // Exit the function early
    }

    // Check if translations exist for the selected language
    if (translations[language]) {
        for (const id of Object.keys(translations[language])) {
            const element = document.getElementById(id + "_dynamiq");
            if (element) {
                const originalText = originalTexts[id + "_dynamiq"];
                const APIUrl = `https://api.mymemory.translated.net/get?q=${encodeURIComponent(originalText)}&langpair=${previousLanguage}|${language}`;
                
                try {
                    const response = await fetch(APIUrl);
                    const data = await response.json();
                    element.innerText = data.responseData.translatedText;
                } catch (error) {
                    console.error('Error fetching translation:', error);
                }
            }

            const staticElement = document.getElementById(id);
            if (staticElement) {
                staticElement.innerHTML = translations[language][id];
            }
        }
    }

    // Update the previous language in sessionStorage
    sessionStorage.setItem('previousLanguage', sessionStorage.getItem('selectedLanguage') || 'en');
    // Update the current language in sessionStorage
    sessionStorage.setItem('selectedLanguage', language);
}

// Event listener for language change
function ChangeSelectLang(value) {
    applyTranslations(value);
}

